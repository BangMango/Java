package encapsulationQuiz;

public class Table {
	private Integer legCnt;
	private Integer circleCnt;
	
	public void setLegCnt(Integer legCnt) {
		this.legCnt=legCnt;
	}
	public void setCircleCnt(Integer circleCnt) {
		this.circleCnt=circleCnt;
	}
	
	public Integer getLegCnt() {
		return legCnt;
	}
	
	public Integer getCircleCnt() {
		return circleCnt;
	}
	
}
